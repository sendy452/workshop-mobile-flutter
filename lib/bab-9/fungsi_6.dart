void main() {
  // Memanggil fungsi functionPerkalian() dengan argumen 5 dan 6
  print(functionPerkalian(5, 6));
}

// Ini adalah fungsi yang menerima dua parameter, 'angka1' dan 'angka2',
// dan mengembalikan hasil perkalian 'angka1' dan 'angka2'.
int functionPerkalian(int angka1, int angka2) {
  return angka1 *
      angka2; // Mengembalikan hasil perkalian 'angka1' dan 'angka2'.
}
